module.exports = function(eleventyConfig) {
  // Static assets
  eleventyConfig.addPassthroughCopy("images");
  eleventyConfig.addPassthroughCopy("style.css");
  eleventyConfig.addPassthroughCopy("netlify");

  // Static HTML pages — copy flat to _site root, skip template processing
  const staticPages = [
    "index.html",
    "about.html",
    "book.html",
    "shop.html",
    "social.html",
    "favorites.html",
    "work-with-me.html",
    "404.html",
    "thank-you-newsletter.html",
    "business-elevation-guide.html",
  ];
  staticPages.forEach((page) => {
    eleventyConfig.addPassthroughCopy(page);
    eleventyConfig.ignores.add(page);
  });

  // Email templates
  eleventyConfig.addPassthroughCopy({ "emails": "emails" });
  eleventyConfig.ignores.add("emails/**");

  // blog.html is generated by blog.njk — ignore the static copy
  eleventyConfig.ignores.add("blog.html");

  // Internal/operational files — never publish
  eleventyConfig.ignores.add(".claude/**");
  eleventyConfig.ignores.add("content-audit/**");
  eleventyConfig.ignores.add("products/**");
  eleventyConfig.ignores.add("node_modules/**");

  // Date filter for post display
  eleventyConfig.addFilter("postDate", (dateStr) => {
    const d = dateStr instanceof Date ? dateStr : new Date(dateStr + "T12:00:00");
    return new Intl.DateTimeFormat("en-US", {
      year: "numeric",
      month: "long",
      day: "numeric",
    }).format(d);
  });

  // Short date for structured data
  eleventyConfig.addFilter("isoDate", (dateStr) => {
    const d = dateStr instanceof Date ? dateStr : new Date(dateStr + "T12:00:00");
    return d.toISOString().slice(0, 10);
  });

  return {
    dir: {
      input: ".",
      output: "_site",
      includes: "_includes",
    },
    htmlTemplateEngine: false,
    markdownTemplateEngine: "njk",
  };
};
